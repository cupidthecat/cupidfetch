static void make_json_key(const char *label, char *out, size_t out_size) {
    if (!out || out_size == 0) return;

    if (!label || !label[0]) {
        snprintf(out, out_size, "unknown");
        return;
    }

    size_t j = 0;
    bool prev_underscore = false;

    for (size_t i = 0; label[i] && j + 1 < out_size; i++) {
        unsigned char c = (unsigned char)label[i];
        if (isalnum(c)) {
            out[j++] = (char)tolower(c);
            prev_underscore = false;
        } else if (!prev_underscore && j > 0) {
            out[j++] = '_';
            prev_underscore = true;
        }
    }

    while (j > 0 && out[j - 1] == '_') {
        j--;
    }

    if (j == 0) {
        snprintf(out, out_size, "unknown");
        return;
    }

    if (isdigit((unsigned char)out[0])) {
        if (j + 1 < out_size) {
            memmove(out + 1, out, j);
            out[0] = '_';
            j++;
        }
    }

    out[j] = '\0';
}

static void print_json_escaped(const char *value) {
    if (!value) {
        printf("\"\"");
        return;
    }

    putchar('"');
    for (const unsigned char *p = (const unsigned char *)value; *p; p++) {
        switch (*p) {
            case '"':
                printf("\\\"");
                break;
            case '\\':
                printf("\\\\");
                break;
            case '\b':
                printf("\\b");
                break;
            case '\f':
                printf("\\f");
                break;
            case '\n':
                printf("\\n");
                break;
            case '\r':
                printf("\\r");
                break;
            case '\t':
                printf("\\t");
                break;
            default:
                if (*p < 0x20) {
                    printf("\\u%04x", *p);
                } else {
                    putchar(*p);
                }
                break;
        }
    }
    putchar('"');
}

