static bool eq_icase(const char *a, const char *b) {
    if (!a || !b) return false;

    while (*a && *b) {
        if (tolower((unsigned char)*a) != tolower((unsigned char)*b)) {
            return false;
        }
        a++;
        b++;
    }
    return *a == '\0' && *b == '\0';
}

static const char *resolve_distro_logo_alias(const char *name) {
    if (!name || !name[0]) return name;

    static const struct {
        const char *alias;
        const char *canonical;
    } alias_map[] = {
        {"ubuntu", "Ubuntu"},
        {"debian", "Debian"},
        {"arch", "Arch"},
        {"mint", "Linux Mint"},
        {"pop", "Pop!_OS"},
        {"kali", "Kali Linux"},
        {"elementary", "elementary OS"},
        {"zorin", "Zorin OS"},
        {"manjaro", "Manjaro"},
        {"endeavouros", "EndeavourOS"},
        {"artix", "Artix Linux"},
        {"fedora", "Fedora"},
        {"alma", "AlmaLinux"},
        {"centos", "CentOS"},
        {"opensuse", "openSUSE"},
        {"alpine", "Alpine Linux"},
        {"gentoo", "Gentoo"},
        {"void", "Void Linux"},
        {"slackware", "Slackware"},
        {"solus", "Solus"},
    };

    for (size_t i = 0; i < sizeof(alias_map) / sizeof(alias_map[0]); i++) {
        if (eq_icase(name, alias_map[i].alias)) {
            return alias_map[i].canonical;
        }
    }

    return name;
}

static bool terminal_supports_truecolor(void) {
    const char *ct = getenv("COLORTERM");
    if (!ct) return false;

    return strstr(ct, "truecolor") != NULL || strstr(ct, "24bit") != NULL;
}

static void print_logo_lines(
    const char *const *lines,
    size_t line_count,
    bool use_truecolor,
    unsigned char r,
    unsigned char g,
    unsigned char b
) {
    if (!lines || line_count == 0) return;

    for (size_t i = 0; i < line_count; i++) {
        if (use_truecolor) {
            printf("\033[38;2;%u;%u;%um%s\033[0m\n", (unsigned)r, (unsigned)g, (unsigned)b, lines[i]);
        } else {
            printf("%s\n", lines[i]);
        }
    }
}

static void print_logo_line_inline(
    const char *line,
    bool use_truecolor,
    unsigned char r,
    unsigned char g,
    unsigned char b
) {
    if (!line) return;

    if (use_truecolor) {
        printf("\033[38;2;%u;%u;%um%s\033[0m", (unsigned)r, (unsigned)g, (unsigned)b, line);
    } else {
        printf("%s", line);
    }
}

static void append_wrapped_line(
    const char *text,
    size_t width,
    char out_lines[][MAX_CAPTURE_LINE_LEN],
    size_t *out_count,
    size_t out_cap
) {
    if (!text || !out_count || width == 0) return;

    size_t len = strlen(text);
    size_t pos = 0;

    if (len == 0) {
        if (*out_count < out_cap) {
            out_lines[*out_count][0] = '\0';
            (*out_count)++;
        }
        return;
    }

    while (pos < len && *out_count < out_cap) {
        size_t remaining = len - pos;
        size_t chunk = remaining > width ? width : remaining;

        if (remaining > width) {
            size_t split = 0;
            for (size_t i = pos + chunk; i > pos; i--) {
                if (text[i - 1] == ' ') {
                    split = i - 1;
                    break;
                }
            }

            if (split > pos) {
                chunk = split - pos;
            }
        }

        if (chunk == 0) {
            chunk = remaining > width ? width : remaining;
        }

        size_t copy_len = chunk < (MAX_CAPTURE_LINE_LEN - 1) ? chunk : (MAX_CAPTURE_LINE_LEN - 1);
        memcpy(out_lines[*out_count], text + pos, copy_len);
        out_lines[*out_count][copy_len] = '\0';

        while (copy_len > 0 && out_lines[*out_count][copy_len - 1] == ' ') {
            out_lines[*out_count][copy_len - 1] = '\0';
            copy_len--;
        }

        (*out_count)++;
        pos += chunk;
        while (pos < len && text[pos] == ' ') pos++;
    }
}

